package com.spring.inno.dto;

public class NoticeVO {
	
	private int notice_num;
	private String notice_title;
	private String notice_content;
	private String notice_view;
	private String notice_regdate;
	private String notice_writer;
	private String notice_test;
	
	
	public int getNotice_num() {
		return notice_num;
	}
	public void setNotice_num(int notice_num) {
		this.notice_num = notice_num;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_view() {
		return notice_view;
	}
	public void setNotice_view(String notice_view) {
		this.notice_view = notice_view;
	}
	public String getNotice_regdate() {
		return notice_regdate;
	}
	public void setNotice_regdate(String notice_regdate) {
		this.notice_regdate = notice_regdate;
	}
	public String getNotice_writer() {
		return notice_writer;
	}
	public void setNotice_writer(String notice_writer) {
		this.notice_writer = notice_writer;
	}
	public String getNotice_test() {
		return notice_test;
	}
	public void setNotice_test(String notice_test) {
		this.notice_test = notice_test;
	}
	
	
	
}
