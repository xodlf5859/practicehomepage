package com.spring.inno.dto;

public class TeamVO {
	
	private int team_num;
	private String team_title;
	private String team_content;
	private String team_view;
	private String team_regdate;
	private String team_writer;
	private String team_category;

	public int getTeam_num() {
		return team_num;
	}
	public void setTeam_num(int team_num) {
		this.team_num = team_num;
	}
	public String getTeam_title() {
		return team_title;
	}
	public void setTeam_title(String team_title) {
		this.team_title = team_title;
	}
	public String getTeam_content() {
		return team_content;
	}
	public void setTeam_content(String team_content) {
		this.team_content = team_content;
	}
	public String getTeam_view() {
		return team_view;
	}
	public void setTeam_view(String team_view) {
		this.team_view = team_view;
	}
	public String getTeam_regdate() {
		return team_regdate;
	}
	public void setTeam_regdate(String team_regdate) {
		this.team_regdate = team_regdate;
	}
	public String getTeam_writer() {
		return team_writer;
	}
	public void setTeam_writer(String team_writer) {
		this.team_writer = team_writer;
	}
	public String getTeam_category() {
		return team_category;
	}
	public void setTeam_category(String team_category) {
		this.team_category = team_category;
	}
	
	
	
}
